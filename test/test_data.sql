-- 4. 验证查询 - 使用JOIN查询完整信息
SELECT a.AREA_ID, a.AREA_SIZE, a.ISEMPTY, ra.RENT_STATUS, ra.BASE_RENT
FROM AREA a
JOIN RETAIL_AREA ra ON a.AREA_ID = ra.AREA_ID
WHERE a.ISEMPTY = 1 AND ra.RENT_STATUS = '空置';

-- 查看账号
SELECT ACCOUNT, USERNAME, IDENTITY, AUTHORITY 
